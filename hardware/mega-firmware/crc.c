uint8_t crc8_p93_n(uint8_t const init, uint8_t const data[], uint8_t const n) {
  uint8_t crc = init;
  for (uint8_t i = 0; i < n; i++) {
    crc = crc8_p93_next(crc, data[i]);
  }
  return crc;
}
